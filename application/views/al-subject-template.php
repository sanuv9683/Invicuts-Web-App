		$("#loader-container").addClass("hide");
		$("#ulQualifications").removeClass("hide");
		$("#exam-years-container a").remove();
		for (var index in subjects) {
			var subject = subjects[index];
			$("#exam-years-container").append("<a href='#div-2017' id='" + subject.yearID + "' class='exam-year'>" + subject.yearName + "</a> |");
		}
